-- THREADSYSTEM
-----------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
	while true do
		local TimeDistance = 999

		if not Active then
			if Cooldown <= GetGameTimer() then
				local Rand = math.random(#Packages)
				Cooldown = GetGameTimer() + (math.random(35, 45) * 60000)
				Nameckage = Name[math.random(#Name)] .. " " .. Lastname[math.random(#Lastname)]

				TriggerEvent("NotifyPush", { code = 20, title = "Pacote Especial", text = "Preciso de você aqui para entregar uma encomenda para minha empresa.", x = Packages[Rand]["x"], y = Packages[Rand]["y"], z = Packages[Rand]["z"], name = Nameckage, blipColor = 2 })

				exports["target"]:RemCircleZone("Packages:Take")
				exports["target"]:AddCircleZone("Packages:Take", Packages[Rand], 0.50, {
					name = "Packages:Take",
					heading = 0.0
				}, {
					Distance = 1.0,
					options = {
						{
							event = "inventory:MakePackage",
							label = "Pegar Pacote",
							tunnel = "products",
							service = "packagebox"
						}
					}
				})
			end
		end

		Wait(TimeDistance)
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- TAKEPACKAGE
-----------------------------------------------------------------------------------------------------------------------------------------
function Hensa.TakePackage()
	if not Active then
		Active = true
		Selected = math.random(#Delivers)
		exports["target"]:RemCircleZone("Packages:Take")

		TriggerEvent("NotifyPush", { code = 20, title = "Entrega Especial", text = "Preciso que você entregue esse pacote na localidade que lhe indiquei, o cliente é muito importante para os negócios.", x = Delivers[Selected]["x"], y = Delivers[Selected]["y"], z = Delivers[Selected]["z"], name = Nameckage, blipColor = 2 })

		exports["target"]:RemCircleZone("Packages:Deliver")
		exports["target"]:AddCircleZone("Packages:Deliver", Delivers[Selected], 1.0, {
			name = "Packages:Deliver",
			heading = 0.0
		}, {
			Distance = 1.0,
			options = {
				{
					event = "inventory:DeliverPackage",
					label = "Entregar Pacote",
					tunnel = "products",
					service = "packagebox"
				}
			}
		})
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- CHECKPACKAGE
-----------------------------------------------------------------------------------------------------------------------------------------
function Hensa.CheckPackage()
	if Active then
		local Ped = PlayerPedId()
		local Coords = GetEntityCoords(Ped)

		if #(Coords - Delivers[Selected]) <= 1.0 then
			Active = false
			Cooldown = GetGameTimer() + (math.random(35, 45) * 60000)

			return true
		end
	end

	return false
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- FINISHPACKAGE
-----------------------------------------------------------------------------------------------------------------------------------------
function Hensa.FinishPackage()
	exports["target"]:RemCircleZone("Packages:Deliver")
end
